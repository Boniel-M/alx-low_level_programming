#include <ctype.h>
/**
 * _isupper - checks if character is uppercase
 *
 * isupper - checks if character is uppercase
 *
 * @c: character to check
 *
 * Return: returns 1 if character is uppercase, 0 otherwise
 *
 */

int _isupper(int c)
{
	if (c == 'A')
		return (1);
	else
		return (0);
}
